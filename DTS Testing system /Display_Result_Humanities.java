    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cts" , "root" , "mysql");
        Statement stmt = con.createStatement() ;

        int a = Integer.parseInt(JOptionPane.showInputDialog("Enter Scholar Number"));

        String s = "Select * from humanities where Sch_Id= "+a+"";

        ResultSet rs = stmt.executeQuery(s);

        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
        int c=m.getRowCount();
        for (int i=0;i<c;i++)
        m.removeRow(0);
        if(rs.next())
        {
        int id = rs.getInt("Sch_Id");
        int his = rs.getInt("history");
        int eco = rs.getInt("eco");
        int geo = rs.getInt("geo");
        int eng = rs.getInt("eng");
        int opt = rs.getInt("opt");

        m.addRow(new Object[]{id,his,geo,eco,eng,opt});
        }else
        {
            JOptionPane.showMessageDialog(rootPane,"Scholar Number "+a+" Does Not Exist In Humanities Section");
        }

         }catch(Exception e)
         {
         JOptionPane.showMessageDialog(rootPane, e);
         }
    }//GEN-LAST:event_jButton3ActionPerformed

