    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        try{
        Class.forName("com.mysql.jdbc.Driver");
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/CTS" , "root" , "mysql");
        Statement stmt = con.createStatement() ;

        int a = Integer.parseInt(JOptionPane.showInputDialog("Enter Scholar Number"));


        String s = "Select * from Commerce where Sch_id="+a+"";

        ResultSet rs = stmt.executeQuery(s);

        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();

        int c=m.getRowCount();
             for (int i=0;i<c;i++)
         m.removeRow(0);
        if(rs.next())
        {
        int id = rs.getInt("Sch_Id");
        int acc = rs.getInt("accounts");
        int eco = rs.getInt("eco");
        int bs = rs.getInt("bs");
        int eng = rs.getInt("eng");
        int opt = rs.getInt("opt");

        m.addRow(new Object[]{id,acc,eco,bs,eng,opt});
        }else
        {
            JOptionPane.showMessageDialog(rootPane,"Scholar Number "+a+" Does Not Exist In Commerce Section");
        }

         }catch(Exception e)
         {
         JOptionPane.showMessageDialog(rootPane, e);
         }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

